package SemesterProject;

public class Table {

	private String label;
	private char status;

	public Table(String label) {

	}

	public String getLabel() {
		return label;
	}
		
	public void setLabel(String label) {
		this.label = label;
	}

	public char getStatus() {
		return status;
	}
		
	public void setStatus(char status) {
		this.status = status;
	}
}
